package com.office.entity;

public class Authority {
    private Integer authorityId;

    private Integer authorityEmp;

    private Integer authorityDept;

    public Integer getAuthorityId() {
        return authorityId;
    }

    public void setAuthorityId(Integer authorityId) {
        this.authorityId = authorityId;
    }

    public Integer getAuthorityEmp() {
        return authorityEmp;
    }

    public void setAuthorityEmp(Integer authorityEmp) {
        this.authorityEmp = authorityEmp;
    }

    public Integer getAuthorityDept() {
        return authorityDept;
    }

    public void setAuthorityDept(Integer authorityDept) {
        this.authorityDept = authorityDept;
    }
}